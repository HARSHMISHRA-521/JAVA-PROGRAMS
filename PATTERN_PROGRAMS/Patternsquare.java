package PATTERN_PROGRAMS;

public class Patternsquare {
    public static void main(String  [] args){
        System.out.println("***** Square pattern *****");
        for(int i=0;i<=6;i++){
            for(int j=0;j<=6;j++){
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
